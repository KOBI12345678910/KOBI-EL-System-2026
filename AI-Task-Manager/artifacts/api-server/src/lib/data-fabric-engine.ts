class ConnectorRegistry {
  constructor(private store: FabricStore) {}

  register(source: Omit<DataSource, "id">): DataSource {
    const id = this.store.nextSourceId++;
    const record: DataSource = { id, ...source };
    this.store.sources.set(id, record);
    this.store.sourcesByKey.set(source.sourceKey, record);
    return record;
  }

  get(idOrKey: number | string): DataSource | undefined {
    if (typeof idOrKey === "number") return this.store.sources.get(idOrKey);
    return this.store.sourcesByKey.get(idOrKey);
  }

  all(): DataSource[] {
    return Array.from(this.store.sources.values());
  }

  byType(type: SourceType): DataSource[] {
    return this.all().filter(s => s.sourceType === type);
  }

  byCategory(category: string): DataSource[] {
    return this.all().filter(s => s.category === category);
  }

  updateStatus(id: number, status: DataSource["status"], healthScore?: number) {
    const s = this.store.sources.get(id);
    if (s) {
      s.status = status;
      if (healthScore != null) s.healthScore = healthScore;
      s.lastSyncAt = new Date();
    }
  }

  health(): { total: number; active: number; failed: number; paused: number; avgHealth: number } {
    const sources = this.all();
    const active = sources.filter(s => s.status === "active").length;
    const failed = sources.filter(s => s.status === "failed").length;
    const paused = sources.filter(s => s.status === "paused").length;
    const withHealth = sources.filter(s => s.healthScore != null);
    const avgHealth = withHealth.length > 0
      ? withHealth.reduce((sum, s) => sum + (s.healthScore ?? 0), 0) / withHealth.length
      : 100;
    return { total: sources.length, active, failed, paused, avgHealth };
  }
}

