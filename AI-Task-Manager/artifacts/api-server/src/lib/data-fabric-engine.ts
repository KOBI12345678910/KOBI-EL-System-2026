class DataProductRegistry {
  constructor(private store: FabricStore) {}

  register(product: Omit<DataProduct, "id">): DataProduct {
    const id = this.store.nextProductId++;
    const record: DataProduct = { id, ...product };
    this.store.products.set(id, record);
    this.store.productsByKey.set(product.productKey, record);
    return record;
  }

  get(idOrKey: number | string): DataProduct | undefined {
    if (typeof idOrKey === "number") return this.store.products.get(idOrKey);
    return this.store.productsByKey.get(idOrKey);
  }

  all(): DataProduct[] {
    return Array.from(this.store.products.values());
  }

  byDomain(domain: string): DataProduct[] {
    return this.all().filter(p => p.domain === domain);
  }

  byStatus(status: DataProduct["status"]): DataProduct[] {
    return this.all().filter(p => p.status === status);
  }
}

