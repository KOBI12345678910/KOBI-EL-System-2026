class IdentityResolutionEngine {
  constructor(private store: FabricStore) {}

  /**
   * Register a record from a source and find-or-create its canonical cluster.
   * Uses exact match on identity fields, then fuzzy fallback.
   */
  resolve(
    canonicalEntity: string,
    sourceId: number,
    sourceRecordId: string,
    attributes: Record<string, unknown>,
    identityFields: string[]
  ): IdentityCluster {
    // Try to find an existing cluster by identity fields
    let match: IdentityCluster | undefined;
    let bestScore = 0;

    for (const cluster of this.store.identityClusters.values()) {
      if (cluster.canonicalEntity !== canonicalEntity) continue;
      const score = this.similarityScore(cluster.canonicalAttributes, attributes, identityFields);
      if (score > bestScore && score >= 0.8) {
        match = cluster;
        bestScore = score;
      }
    }

    if (match) {
      // Add the link
      match.sourceCount++;
      match.links.push({
        sourceId,
        sourceRecordId,
        sourceAttributes: attributes,
        matchScore: bestScore,
      });
      if (match.confidence) {
        match.confidence = (match.confidence + bestScore) / 2;
      } else {
        match.confidence = bestScore;
      }
      return match;
    }

    // Create new cluster
    const id = this.store.nextClusterId++;
    const canonicalId = `${canonicalEntity}-${id}`;
    const newCluster: IdentityCluster = {
      id,
      canonicalEntity,
      canonicalId,
      canonicalAttributes: { ...attributes },
      sourceCount: 1,
      confidence: 1,
      resolutionMethod: "exact",
      links: [{ sourceId, sourceRecordId, sourceAttributes: attributes, matchScore: 1 }],
    };
    this.store.identityClusters.set(`${canonicalEntity}:${canonicalId}`, newCluster);
    return newCluster;
  }

  private similarityScore(
    a: Record<string, unknown>,
    b: Record<string, unknown>,
    fields: string[]
  ): number {
    if (fields.length === 0) return 0;
    let matches = 0;
    for (const field of fields) {
      const va = this.normalize(a[field]);
      const vb = this.normalize(b[field]);
      if (va && vb && va === vb) matches++;
      else if (va && vb && this.levenshteinSimilarity(va, vb) > 0.85) matches += 0.85;
    }
    return matches / fields.length;
  }

  private normalize(v: unknown): string {
    if (v == null) return "";
    return String(v).toLowerCase().trim().replace(/\s+/g, " ");
  }

  private levenshteinSimilarity(a: string, b: string): number {
    if (a === b) return 1;
    const maxLen = Math.max(a.length, b.length);
    if (maxLen === 0) return 1;
    const dist = this.levenshteinDistance(a, b);
    return 1 - dist / maxLen;
  }

  private levenshteinDistance(a: string, b: string): number {
    const dp = Array.from({ length: a.length + 1 }, () => new Array(b.length + 1).fill(0));
    for (let i = 0; i <= a.length; i++) dp[i]![0] = i;
    for (let j = 0; j <= b.length; j++) dp[0]![j] = j;
    for (let i = 1; i <= a.length; i++) {
      for (let j = 1; j <= b.length; j++) {
        const cost = a[i - 1] === b[j - 1] ? 0 : 1;
        dp[i]![j] = Math.min(
          dp[i - 1]![j]! + 1,
          dp[i]![j - 1]! + 1,
          dp[i - 1]![j - 1]! + cost
        );
      }
    }
    return dp[a.length]![b.length]!;
  }

  allClusters(): IdentityCluster[] {
    return Array.from(this.store.identityClusters.values());
  }

  clustersForEntity(entityKey: string): IdentityCluster[] {
    return this.allClusters().filter(c => c.canonicalEntity === entityKey);
  }

  stats() {
    const clusters = this.allClusters();
    return {
      totalClusters: clusters.length,
      totalLinks: clusters.reduce((sum, c) => sum + c.links.length, 0),
      multiSourceClusters: clusters.filter(c => c.sourceCount > 1).length,
      avgConfidence: clusters.length > 0
        ? clusters.reduce((sum, c) => sum + (c.confidence ?? 0), 0) / clusters.length
        : 0,
    };
  }
}

