// QUALITY ENGINE
// ════════════════════════════════════════════════════════════════

class QualityEngine {
  constructor(private store: FabricStore) {}

  registerRule(rule: Omit<QualityRule, "id">): QualityRule {
    const id = this.store.nextQualityRuleId++;
    const record: QualityRule = { id, ...rule };
    this.store.qualityRules.set(id, record);
    return record;
  }

  allRules(): QualityRule[] {
    return Array.from(this.store.qualityRules.values());
  }

  rulesForDataset(datasetKey: string): QualityRule[] {
    return this.allRules().filter(r => r.datasetKey === datasetKey);
  }

  recordResult(result: Omit<QualityResult, "id" | "executedAt"> & { executedAt?: Date }): QualityResult {
    const id = this.store.nextQualityResultId++;
    const record: QualityResult = {
      id,
      ...result,
      executedAt: result.executedAt ?? new Date(),
    };
    this.store.qualityResults.push(record);
    if (this.store.qualityResults.length > this.store.maxQualityResults) {
      this.store.qualityResults.shift();
    }
    // Publish quality event if fail
    if (result.status === "fail" || result.status === "error") {
      const rule = this.store.qualityRules.get(result.ruleId);
      realtimePlatform.publish({
        eventType: "data_quality.failed",
        sourceModule: "ai",
        entityType: "quality_rule",
        entityId: String(result.ruleId),
        entityLabel: rule?.name ?? `rule-${result.ruleId}`,
        severity: rule?.severity === "critical" || rule?.severity === "blocking" ? "critical" : "warning",
        newState: { status: result.status, rowsFailed: result.rowsFailed },
      });
    }
    return record;
  }

  recentResults(limit = 100): QualityResult[] {
    return this.store.qualityResults.slice(-limit).reverse();
  }

  datasetScore(datasetKey: string): number {
    const results = this.store.qualityResults.filter(r => r.datasetKey === datasetKey).slice(-20);
    if (results.length === 0) return 100;
    const passRate = results.filter(r => r.status === "pass").length / results.length;
    return passRate * 100;
  }

  summary() {
    const results = this.store.qualityResults;
    const pass = results.filter(r => r.status === "pass").length;
    const warn = results.filter(r => r.status === "warn").length;
    const fail = results.filter(r => r.status === "fail").length;
    const error = results.filter(r => r.status === "error").length;
    return {
      totalChecks: results.length,
      pass,
      warn,
      fail,
      error,
      passRate: results.length > 0 ? (pass / results.length) * 100 : 0,
      activeRules: this.allRules().filter(r => r.enabled).length,
    };
  }
}

