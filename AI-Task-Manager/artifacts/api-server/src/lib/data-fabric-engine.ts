// WRITE-BACK ENGINE
// ════════════════════════════════════════════════════════════════

class WriteBackEngine {
  constructor(private store: FabricStore) {}

  queue(action: Omit<WriteBackAction, "id" | "status" | "createdAt">): WriteBackAction {
    const id = this.store.nextWriteBackId++;
    const record: WriteBackAction = {
      id,
      ...action,
      status: action.requiresApproval ? "queued" : "queued",
      createdAt: new Date(),
    };
    this.store.writeBackActions.push(record);
    return record;
  }

  async execute(actionId: number): Promise<WriteBackAction> {
    const action = this.store.writeBackActions.find(a => a.id === actionId);
    if (!action) throw new Error(`Action ${actionId} not found`);

    if (action.requiresApproval && !action.approvedBy) {
      action.status = "failed";
      action.errorMessage = "Approval required";
      return action;
    }

    action.status = "running";
    action.executedAt = new Date();

    // Simulate execution
    await new Promise(r => setTimeout(r, 20));

    action.status = "success";
    action.result = { success: true, actionType: action.actionType };

    realtimePlatform.publish({
      eventType: "write_back.executed",
      sourceModule: "ai",
      entityType: "write_back",
      entityId: String(actionId),
      entityLabel: `${action.actionType} → source ${action.targetSourceKey}`,
      severity: "success",
      newState: { status: "success" },
    });

    return action;
  }

  approve(actionId: number, userId: number) {
    const action = this.store.writeBackActions.find(a => a.id === actionId);
    if (action) {
      action.approvedBy = userId;
      action.approvedAt = new Date();
    }
    return action;
  }

  recent(limit = 100): WriteBackAction[] {
    return this.store.writeBackActions.slice(-limit).reverse();
  }

  pending(): WriteBackAction[] {
    return this.store.writeBackActions.filter(a => a.status === "queued" && a.requiresApproval && !a.approvedBy);
  }
}

