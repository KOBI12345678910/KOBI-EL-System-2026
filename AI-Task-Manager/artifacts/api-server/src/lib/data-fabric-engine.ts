class IngestionEngine {
  constructor(private store: FabricStore) {}

  registerJob(job: Omit<IngestionJob, "id">): IngestionJob {
    const id = this.store.nextJobId++;
    const record: IngestionJob = { id, totalRowsIngested: 0, ...job };
    this.store.ingestionJobs.set(id, record);
    return record;
  }

  allJobs(): IngestionJob[] {
    return Array.from(this.store.ingestionJobs.values());
  }

  getJob(id: number): IngestionJob | undefined {
    return this.store.ingestionJobs.get(id);
  }

  jobsByMode(mode: IngestionMode): IngestionJob[] {
    return this.allJobs().filter(j => j.mode === mode);
  }

  async runJob(jobId: number): Promise<IngestionRun> {
    const job = this.store.ingestionJobs.get(jobId);
    if (!job) throw new Error(`Job ${jobId} not found`);

    const runId = this.store.nextRunId++;
    const run: IngestionRun = {
      id: runId,
      jobId,
      status: "running",
      startedAt: new Date(),
      rowsRead: 0,
      rowsWritten: 0,
      rowsSkipped: 0,
      rowsFailed: 0,
    };
    this.store.ingestionRuns.push(run);
    if (this.store.ingestionRuns.length > this.store.maxRuns) this.store.ingestionRuns.shift();

    // Publish ingestion started event
    realtimePlatform.publish({
      eventType: "ingestion.started",
      sourceModule: "ai",
      entityType: "ingestion_job",
      entityId: String(jobId),
      entityLabel: job.name,
      severity: "info",
      metadata: { mode: job.mode, zone: job.targetZone },
    });

    // Simulate execution
    await new Promise(r => setTimeout(r, 20));
    const rowsRead = Math.floor(Math.random() * 1000) + 100;
    const rowsWritten = Math.floor(rowsRead * 0.95);
    const rowsFailed = rowsRead - rowsWritten;
    run.finishedAt = new Date();
    run.durationMs = run.finishedAt.getTime() - (run.startedAt?.getTime() ?? 0);
    run.status = "success";
    run.rowsRead = rowsRead;
    run.rowsWritten = rowsWritten;
    run.rowsFailed = rowsFailed;
    run.watermarkEnd = new Date().toISOString();

    job.lastRunAt = run.finishedAt;
    job.lastSuccessAt = run.finishedAt;
    job.lastRowsIngested = rowsWritten;
    job.totalRowsIngested = (job.totalRowsIngested ?? 0) + rowsWritten;

    // Publish completion event
    realtimePlatform.publish({
      eventType: "ingestion.success",
      sourceModule: "ai",
      entityType: "ingestion_job",
      entityId: String(jobId),
      entityLabel: job.name,
      severity: "success",
      newState: { status: "success", rowsWritten, durationMs: run.durationMs },
      metadata: { runId, targetZone: job.targetZone },
    });

    return run;
  }

  recentRuns(limit = 100): IngestionRun[] {
    return this.store.ingestionRuns.slice(-limit).reverse();
  }

  runsByJob(jobId: number, limit = 50): IngestionRun[] {
    return this.store.ingestionRuns.filter(r => r.jobId === jobId).slice(-limit).reverse();
  }

  stats() {
    const runs = this.store.ingestionRuns;
    const total = runs.length;
    const success = runs.filter(r => r.status === "success").length;
    const failed = runs.filter(r => r.status === "failed").length;
    const running = runs.filter(r => r.status === "running").length;
    return {
      total,
      success,
      failed,
      running,
      successRate: total > 0 ? (success / total) * 100 : 0,
      totalRowsIngested: runs.reduce((sum, r) => sum + r.rowsWritten, 0),
    };
  }
}

