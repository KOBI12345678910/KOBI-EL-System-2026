// LINEAGE ENGINE
// ════════════════════════════════════════════════════════════════

class LineageEngine {
  constructor(private store: FabricStore) {}

  addEdge(edge: Omit<LineageEdge, "id" | "observedAt"> & { observedAt?: Date }): LineageEdge {
    const id = this.store.nextLineageId++;
    const record: LineageEdge = {
      id,
      ...edge,
      observedAt: edge.observedAt ?? new Date(),
    };
    this.store.lineage.push(record);
    return record;
  }

  downstream(fromType: string, fromId: string, maxDepth = 5): LineageEdge[] {
    const visited = new Set<string>();
    const result: LineageEdge[] = [];
    const queue: Array<{ type: string; id: string; depth: number }> = [
      { type: fromType, id: fromId, depth: 0 },
    ];
    visited.add(`${fromType}:${fromId}`);

    while (queue.length > 0) {
      const cur = queue.shift()!;
      if (cur.depth >= maxDepth) continue;
      const edges = this.store.lineage.filter(
        e => e.fromType === cur.type && e.fromId === cur.id
      );
      for (const edge of edges) {
        const key = `${edge.toType}:${edge.toId}`;
        if (visited.has(key)) continue;
        visited.add(key);
        result.push(edge);
        queue.push({ type: edge.toType, id: edge.toId, depth: cur.depth + 1 });
      }
    }
    return result;
  }

  upstream(toType: string, toId: string, maxDepth = 5): LineageEdge[] {
    const visited = new Set<string>();
    const result: LineageEdge[] = [];
    const queue: Array<{ type: string; id: string; depth: number }> = [
      { type: toType, id: toId, depth: 0 },
    ];
    visited.add(`${toType}:${toId}`);

    while (queue.length > 0) {
      const cur = queue.shift()!;
      if (cur.depth >= maxDepth) continue;
      const edges = this.store.lineage.filter(
        e => e.toType === cur.type && e.toId === cur.id
      );
      for (const edge of edges) {
        const key = `${edge.fromType}:${edge.fromId}`;
        if (visited.has(key)) continue;
        visited.add(key);
        result.push(edge);
        queue.push({ type: edge.fromType, id: edge.fromId, depth: cur.depth + 1 });
      }
    }
    return result;
  }

  allEdges(): LineageEdge[] {
    return this.store.lineage.slice();
  }

  graphSummary() {
    const nodeSet = new Set<string>();
    for (const e of this.store.lineage) {
      nodeSet.add(`${e.fromType}:${e.fromId}`);
      nodeSet.add(`${e.toType}:${e.toId}`);
    }
    return { nodes: nodeSet.size, edges: this.store.lineage.length };
  }
}

