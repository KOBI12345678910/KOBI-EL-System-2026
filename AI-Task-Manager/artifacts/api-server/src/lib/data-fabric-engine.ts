// FRESHNESS TRACKER
// ════════════════════════════════════════════════════════════════

class FreshnessTracker {
  constructor(private store: FabricStore) {}

  measure(datasetKey: string): FreshnessMeasurement {
    const dataset = this.store.datasets.get(datasetKey);
    const now = Date.now();
    const lastRefresh = dataset?.lastRefreshedAt?.getTime() ?? 0;
    const actualLagSeconds = Math.floor((now - lastRefresh) / 1000);
    const slaMinutes = dataset?.freshnessSlaMinutes ?? 60;
    const slaSeconds = slaMinutes * 60;
    let status: FreshnessStatus = "fresh";
    if (!lastRefresh) status = "missing";
    else if (actualLagSeconds > slaSeconds * 2) status = "stale";
    else if (actualLagSeconds > slaSeconds) status = "warning";

    const measurement: FreshnessMeasurement = {
      datasetKey,
      slaMinutes,
      actualLagSeconds,
      status,
      measuredAt: new Date(),
    };
    this.store.freshnessMeasurements.set(datasetKey, measurement);
    return measurement;
  }

  current(datasetKey: string): FreshnessMeasurement | undefined {
    return this.store.freshnessMeasurements.get(datasetKey);
  }

  all(): FreshnessMeasurement[] {
    return Array.from(this.store.freshnessMeasurements.values());
  }

  scanAll(): FreshnessMeasurement[] {
    return Array.from(this.store.datasets.keys()).map(key => this.measure(key));
  }

  summary() {
    const measurements = this.all();
    const fresh = measurements.filter(m => m.status === "fresh").length;
    const warning = measurements.filter(m => m.status === "warning").length;
    const stale = measurements.filter(m => m.status === "stale").length;
    const missing = measurements.filter(m => m.status === "missing").length;
    return { total: measurements.length, fresh, warning, stale, missing };
  }
}

