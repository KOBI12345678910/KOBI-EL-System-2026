class PipelineEngine {
  constructor(private store: FabricStore) {}

  register(pipeline: Omit<FabricPipeline, "id">): FabricPipeline {
    const id = this.store.nextPipelineId++;
    const record: FabricPipeline = { id, ...pipeline };
    this.store.pipelines.set(id, record);
    this.store.pipelinesByKey.set(pipeline.pipelineKey, record);
    return record;
  }

  get(idOrKey: number | string): FabricPipeline | undefined {
    if (typeof idOrKey === "number") return this.store.pipelines.get(idOrKey);
    return this.store.pipelinesByKey.get(idOrKey);
  }

  all(): FabricPipeline[] {
    return Array.from(this.store.pipelines.values());
  }

  async run(pipelineId: number): Promise<{ success: boolean; durationMs: number; nodesExecuted: number }> {
    const p = this.store.pipelines.get(pipelineId);
    if (!p) throw new Error(`Pipeline ${pipelineId} not found`);

    const start = Date.now();
    realtimePlatform.publish({
      eventType: "pipeline.started",
      sourceModule: "ai",
      entityType: "pipeline",
      entityId: String(pipelineId),
      entityLabel: p.name,
      severity: "info",
    });

    // Topological execution (simulated)
    const nodes = p.dag?.nodes ?? [];
    await new Promise(r => setTimeout(r, 30));
    const durationMs = Date.now() - start;

    p.lastRunAt = new Date();
    p.lastSuccessAt = new Date();
    p.avgDurationMs = p.avgDurationMs ? (p.avgDurationMs + durationMs) / 2 : durationMs;
    p.successRate = 100;

    realtimePlatform.publish({
      eventType: "pipeline.success",
      sourceModule: "ai",
      entityType: "pipeline",
      entityId: String(pipelineId),
      entityLabel: p.name,
      severity: "success",
      newState: { status: "success", durationMs, nodesExecuted: nodes.length },
    });

    return { success: true, durationMs, nodesExecuted: nodes.length };
  }
}

