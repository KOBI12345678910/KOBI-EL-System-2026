// CHANGE EVENT STORE — generic CDC
// ════════════════════════════════════════════════════════════════

class ChangeEventStore {
  constructor(private store: FabricStore) {}

  record(event: Omit<ChangeEvent, "id" | "receivedAt">): ChangeEvent {
    const id = this.store.nextChangeId++;
    const record: ChangeEvent = {
      id,
      ...event,
      receivedAt: new Date(),
    };
    this.store.changeEvents.push(record);
    if (this.store.changeEvents.length > this.store.maxChangeEvents) {
      this.store.changeEvents.shift();
    }
    return record;
  }

  recent(limit = 100): ChangeEvent[] {
    return this.store.changeEvents.slice(-limit).reverse();
  }

  bySource(sourceId: number, limit = 100): ChangeEvent[] {
    return this.store.changeEvents.filter(e => e.sourceId === sourceId).slice(-limit).reverse();
  }

  byDataset(datasetKey: string, limit = 100): ChangeEvent[] {
    return this.store.changeEvents.filter(e => e.datasetKey === datasetKey).slice(-limit).reverse();
  }

  stats() {
    const events = this.store.changeEvents;
    const byOp: Record<string, number> = {};
    for (const e of events) byOp[e.operation] = (byOp[e.operation] ?? 0) + 1;
    return { total: events.length, byOperation: byOp };
  }
}

