'use strict';

const { test, describe } = require('node:test');
const assert = require('node:assert/strict');
const path = require('path');

const {
  validateIban,
  parseIsraeliIban,
  formatIban,
  normalizeIban,
  israeliBanks,
  IBAN_COUNTRY_LENGTHS,
} = require(path.resolve(__dirname, '..', '..', 'src', 'validators', 'iban.js'));

// ═════════════════════════════════════════════════════════════
// Known-good IBAN test vectors
// ═════════════════════════════════════════════════════════════
//
// IL vectors below were generated by computing the ISO 13616 check
// digits for a constructed BBAN, so each is guaranteed to satisfy
// mod-97 === 1. See the agent 92 QA report for the generator.
//
// IL05 0108 0000 0012 3456 789   — Leumi (10)       / לאומי
// IL58 0125 3534 5678 9012 345   — Mizrahi-Tef (12) / מזרחי טפחות
// IL36 0204 0400 0000 1234 567   — Mizrachi (20)    / מזרחי
// IL19 0311 2300 0045 6789 012   — Hapoalim (31)    / הפועלים
// IL98 0541 0000 0000 0012 345   — Jerusalem (54)   / ירושלים
// IL33 0110 0912 3456 7890 123   — Discount (11)    / דיסקונט
// IL80 0130 1598 7654 3210 987   — Igud (13)        / איגוד
// IL90 0522 3400 0098 7654 321   — Poalei Agudat (52) / פועלי אגודת ישראל

const IL_LEUMI       = 'IL050108000000123456789';
const IL_MIZRAHI_TEF = 'IL580125353456789012345';
const IL_MIZRACHI    = 'IL360204040000001234567';
const IL_HAPOALIM    = 'IL190311230000456789012';
const IL_JERUSALEM   = 'IL980541000000000012345';
const IL_DISCOUNT    = 'IL330110091234567890123';
const IL_IGUD        = 'IL800130159876543210987';
const IL_POALEI_AG   = 'IL900522340000987654321';

// Official ECBS test vectors for non-IL countries.
const DE_VALID = 'DE89370400440532013000';
const GB_VALID = 'GB82WEST12345698765432';
const FR_VALID = 'FR1420041010050500013M02606';
const CH_VALID = 'CH9300762011623852957';

// ═════════════════════════════════════════════════════════════
// SECTION 1 — ISO 13616 core: valid IBANs
// ═════════════════════════════════════════════════════════════

describe('IBAN.1 ISO 13616 valid vectors', () => {
  test('1.01 German IBAN (ECBS vector)', () => {
    const r = validateIban(DE_VALID);
    assert.equal(r.valid, true, `expected valid DE IBAN, got ${r.reason}`);
    assert.equal(r.country, 'DE');
  });

  test('1.02 UK IBAN (ECBS vector)', () => {
    const r = validateIban(GB_VALID);
    assert.equal(r.valid, true);
    assert.equal(r.country, 'GB');
  });

  test('1.03 French IBAN with embedded letter in account', () => {
    // FR IBAN explicitly permits letters in the BBAN ("013M0"), which
    // exercises the A=10..Z=35 conversion branch.
    const r = validateIban(FR_VALID);
    assert.equal(r.valid, true, `FR IBAN failed: ${r.reason}`);
    assert.equal(r.country, 'FR');
  });

  test('1.04 Swiss IBAN', () => {
    const r = validateIban(CH_VALID);
    assert.equal(r.valid, true);
    assert.equal(r.country, 'CH');
  });
});

