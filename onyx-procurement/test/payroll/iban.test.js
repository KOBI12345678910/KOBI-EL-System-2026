// SECTION 6 — israeliBanks map
// ═════════════════════════════════════════════════════════════

describe('IBAN.6 israeliBanks map', () => {
  test('6.01 required banks present per agent 92 spec', () => {
    const required = [10, 11, 12, 13, 14, 17, 20, 26, 31, 46, 52, 54, 77];
    for (const code of required) {
      assert.ok(israeliBanks[code], `missing bank code ${code}`);
      assert.ok(israeliBanks[code].name_en, `bank ${code} missing name_en`);
      assert.ok(israeliBanks[code].name_he, `bank ${code} missing name_he`);
    }
  });

  test('6.02 map is frozen (never delete rule)', () => {
    assert.throws(() => { israeliBanks[10] = null; });
    assert.throws(() => { delete israeliBanks[10]; });
  });

  test('6.03 IBAN_COUNTRY_LENGTHS is frozen', () => {
    assert.throws(() => { IBAN_COUNTRY_LENGTHS.IL = 99; });
  });

  test('6.04 Hebrew names are real Hebrew (bidi check)', () => {
    // Rough sanity: every Hebrew name should contain at least one code
    // point in the Hebrew block U+0590..U+05FF.
    for (const code of Object.keys(israeliBanks)) {
      const he = israeliBanks[code].name_he;
      const hasHebrew = [...he].some(c => {
        const cp = c.codePointAt(0);
        return cp >= 0x0590 && cp <= 0x05FF;
      });
      // Allow "SBI" etc. Latin-only names as a special case.
      if (!hasHebrew) {
        assert.ok(
          /^[A-Za-z0-9\s]+$/.test(he),
          `bank ${code} name_he is neither Hebrew nor Latin: ${he}`
        );
      }
    }
  });
});

// ═════════════════════════════════════════════════════════════
// SECTION 7 — Round-trip & invariants
// ═════════════════════════════════════════════════════════════

describe('IBAN.7 round-trip invariants', () => {
  test('7.01 format → normalize → validate is stable', () => {
    const formatted = formatIban(IL_LEUMI);
    const back = normalizeIban(formatted);
    assert.equal(back, IL_LEUMI);
    assert.equal(validateIban(formatted).valid, true);
  });

  test('7.02 every seeded IL vector round-trips', () => {
    const vectors = [
      IL_LEUMI, IL_MIZRAHI_TEF, IL_MIZRACHI, IL_HAPOALIM,
      IL_JERUSALEM, IL_DISCOUNT, IL_IGUD, IL_POALEI_AG,
    ];
    for (const v of vectors) {
      const r = validateIban(v);
      assert.equal(r.valid, true, `vector ${v} failed: ${r.reason}`);
      assert.equal(r.country, 'IL');
      assert.equal(r.normalized, v);
      assert.equal(normalizeIban(formatIban(v)), v);
    }
  });

  test('7.03 validating a number primitive returns bad_format, not crash', () => {
    const r = validateIban(12345);
    assert.equal(r.valid, false);
    // Number coerces to "12345" which is not 2 letters + digits.
    assert.equal(r.reason, 'bad_format');
  });

  test('7.04 validating an object does not crash', () => {
    const r = validateIban({ foo: 'bar' });
    assert.equal(r.valid, false);
    // Object stringifies to "[OBJECT OBJECT]" etc — should reject shape.
    assert.ok(['bad_format', 'bad_length'].includes(r.reason));
  });
});
